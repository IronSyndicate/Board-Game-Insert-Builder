// ── Schema-driven SCAD generator ─────────────────────────────────────────────
import {
  GLOBAL_GROUPS, LBL_FIELDS, LID_GROUPS, CMP_GROUPS,
  BOX_GROUPS, HEXBOX_GROUPS, SPACER_GROUPS, DIV_GROUPS, flat,
} from './schema.js';

const esc = s => String(s).replace(/\\/g, '\\\\').replace(/"/g, '\\"');
const num = v => {
  const n = parseFloat(v);
  return Number.isFinite(n) ? +n.toFixed(4) : 0;
};
const sb = v => (v ? 't' : 'f');
const posv = v => (v === 'CENTER' || v === 'MAX' ? v : num(v));

// Format a model value for SCAD output, per field type.
function fmt(f, v) {
  switch (f.t) {
    case 'num': return String(num(v));
    case 'bool': return sb(v);
    case 'str': return `"${esc(v)}"`;
    case 'enum': return f.strv ? `"${esc(v)}"` : String(v);
    case 'vec2': return `[${num(v[0])}, ${num(v[1])}]`;
    case 'vec3': return `[${num(v[0])}, ${num(v[1])}, ${num(v[2])}]`;
    case 'bool4': return `[${v.map(sb).join(', ')}]`;
    case 'num4': return `[${v.map(num).join(', ')}]`;
    case 'posxy': return `[${posv(v[0])}, ${posv(v[1])}]`;
    case 'size': return (v === 'AUTO' || v === '' || v == null) ? 'AUTO' : String(num(v));
    case 'list': return `[${v.map(s => `"${esc(s)}"`).join(', ')}]`;
    case 'ltext': {
      const s = String(v);
      if (s.includes('\n') || s.includes('|')) {
        const rows = s.split('\n').map(r => `[${r.split('|').map(c => `"${esc(c.trim())}"`).join(', ')}]`);
        return `[ ${rows.join(', ')} ]`;
      }
      return `"${esc(s)}"`;
    }
    default: return String(v);
  }
}

const isEmpty = v => v == null || v === '';
const eq = (a, b) => JSON.stringify(a) === JSON.stringify(b);

// Emit `[ KEY, value ],` lines for every field whose value differs from the
// library default (or is flagged `always`).
function emitFields(obj, fields, pad) {
  const out = [];
  for (const f of fields) {
    const v = obj[f.m];
    if (isEmpty(v)) continue;
    if (f.t === 'ltext' || f.t === 'str') { if (!String(v).trim()) continue; }
    if (!f.always && eq(v, f.d)) continue;
    out.push(`${pad}[ ${f.k}, ${fmt(f, v)} ],`);
  }
  return out;
}

function emitLabel(lbl, pad) {
  const has = lbl.mode === 'image' ? String(lbl.image || '').trim() : String(lbl.text || '').trim();
  if (!has) return [];
  const fields = LBL_FIELDS.filter(f => (lbl.mode === 'image' ? f.m !== 'text' : f.m !== 'image'));
  return [`${pad}[ LABEL,`, `${pad}    [`,
    ...emitFields(lbl, fields, pad + '        '),
    `${pad}    ]`, `${pad}],`];
}

function emitLid(lid, pad) {
  const lines = emitFields(lid, flat(LID_GROUPS), pad + '        ');
  for (const lbl of lid.labels || []) lines.push(...emitLabel(lbl, pad + '        '));
  return [`${pad}[ BOX_LID,`, `${pad}    [`, ...lines, `${pad}    ]`, `${pad}],`];
}

function emitComponent(cmp, pad) {
  if (cmp.enabled === false) return [];
  const lines = emitFields(cmp, flat(CMP_GROUPS), pad + '        ');
  for (const lbl of cmp.labels || []) lines.push(...emitLabel(lbl, pad + '        '));
  return [`${pad}[ BOX_COMPONENT,`, `${pad}    [`, ...lines, `${pad}    ]`, `${pad}],`];
}

function emitBox(box) {
  const pad = '        ';
  const L = [`    [   "${esc(box.name)}",`, '        ['];
  if (box.enabled === false) L.push(`${pad}    [ ENABLED_B, f ],`);

  if (box.type === 'BOX') {
    L.push(...emitFields(box, flat(BOX_GROUPS), pad + '    '));
    for (const lbl of box.labels || []) L.push(...emitLabel(lbl, pad + '    '));
    for (const cmp of box.components || []) L.push(...emitComponent(cmp, pad + '    '));
    if (!box.no_lid && box.lid?.enabled !== false) L.push(...emitLid(box.lid || {}, pad + '    '));
  } else if (box.type === 'HEXBOX') {
    L.push(`${pad}    [ TYPE, HEXBOX ],`);
    L.push(...emitFields(box, flat(HEXBOX_GROUPS), pad + '    '));
    for (const lbl of box.labels || []) L.push(...emitLabel(lbl, pad + '    '));
    for (const cmp of box.components || []) L.push(...emitComponent(cmp, pad + '    '));
    if (box.lid?.enabled !== false) L.push(...emitLid(box.lid || {}, pad + '    '));
  } else if (box.type === 'SPACER') {
    L.push(`${pad}    [ TYPE, SPACER ],`);
    L.push(...emitFields(box, flat(SPACER_GROUPS), pad + '    '));
  } else if (box.type === 'DIVIDERS') {
    L.push(`${pad}    [ TYPE, DIVIDERS ],`);
    L.push(`${pad}    [ DIV_TAB_TEXT, ${fmt({ t: 'list' }, box.tab_texts || [])} ],`);
    L.push(...emitFields(box, flat(DIV_GROUPS), pad + '    '));
  }

  L.push('        ]', '    ],');
  return L;
}

export function generateScad(project) {
  const s = project.settings || {};
  const L = [
    '// Generated by BIT Builder',
    'include <boardgame_insert_toolkit_lib.3.scad>;',
    'include <bit_functions_lib.3.scad>;',
    '',
  ];
  for (const f of flat(GLOBAL_GROUPS)) {
    const v = s[f.m];
    if (isEmpty(v)) continue;
    if (!f.always && eq(v, f.d)) continue;
    const val = f.t === 'bool' ? String(!!v)
      : f.t === 'num' ? String(num(v))
      : `"${esc(v)}"`;
    L.push(`${f.g} = ${val};`);
  }
  if (s.isolated_box) L.push(`g_isolated_print_box = "${esc(s.isolated_box)}";`);
  L.push('', 'data =', '[');
  for (const box of project.boxes || []) L.push(...emitBox(box), '');
  L.push('];', '', 'MakeAll();', '');
  return L.join('\n');
}

// ── Validation (fit warnings for the UI) ─────────────────────────────────────
export function componentFootprint(cmp) {
  const [nx, ny] = cmp.num, [cx, cy] = cmp.size, [px, py] = cmp.padding;
  const [mf, mb, ml, mr] = cmp.margin;
  return {
    w: nx * cx + (nx - 1) * px + ml + mr,
    h: ny * cy + (ny - 1) * py + mf + mb,
  };
}

export function validateProject(project) {
  const warns = [];
  const wall = num(project.settings?.wall_thickness ?? 1.5);
  for (const box of project.boxes || []) {
    if (box.enabled === false) continue;
    if (box.type !== 'BOX' && box.type !== 'HEXBOX') continue;
    const w = num(box.wall_override) || wall;
    const inner = box.type === 'BOX'
      ? [box.size[0] - 2 * w, box.size[1] - 2 * w, box.size[2] - w]
      : [box.hex_size[0], box.hex_size[0] * Math.sin(Math.PI / 3), box.hex_size[1] - w];
    for (const cmp of box.components || []) {
      if (cmp.enabled === false) continue;
      const { w: fw, h: fh } = componentFootprint(cmp);
      const tag = `"${box.name}" › "${cmp.name}"`;
      if (fw > inner[0] + 0.001 || fh > inner[1] + 0.001)
        warns.push(`${tag}: footprint ${fw.toFixed(1)}×${fh.toFixed(1)}mm exceeds interior ${inner[0].toFixed(1)}×${inner[1].toFixed(1)}mm.`);
      if (num(cmp.size[2]) > inner[2] + 0.001)
        warns.push(`${tag}: depth ${cmp.size[2]}mm exceeds interior depth ${inner[2].toFixed(1)}mm.`);
      const px = cmp.position[0], py = cmp.position[1];
      const offX = (px === 'CENTER') ? (box.size[0] - fw) / 2 - w : (px === 'MAX') ? inner[0] - fw : num(px);
      const offY = (py === 'CENTER') ? (box.size[1] - fh) / 2 - w : (py === 'MAX') ? inner[1] - fh : num(py);
      if (box.type === 'BOX' && (offX < -0.001 || offY < -0.001 || offX + fw > inner[0] + 0.001 || offY + fh > inner[1] + 0.001))
        warns.push(`${tag}: position pushes it outside the compartment interior.`);
    }
  }
  return warns;
}

// Resolve a component's offset within the box interior (mm, from interior front-left).
export function componentOffset(box, cmp, wall) {
  const { w: fw, h: fh } = componentFootprint(cmp);
  const innerW = box.size[0] - 2 * wall, innerH = box.size[1] - 2 * wall;
  const px = cmp.position[0], py = cmp.position[1];
  return {
    x: px === 'CENTER' ? (innerW - fw) / 2 : px === 'MAX' ? innerW - fw : num(px),
    y: py === 'CENTER' ? (innerH - fh) / 2 : py === 'MAX' ? innerH - fh : num(py),
    fw, fh,
  };
}
